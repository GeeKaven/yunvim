return {
  -- snippets
  {
    "L3MON4D3/LuaSnip",
    build = (not jit.os:find("Windows"))
        and "echo -e 'NOTE: jsregexp is optional, so not a big deal if it fails to build\n'; make install_jsregexp"
        or nil,
    dependencies = {
      "rafamadriz/friendly-snippets",
      config = function()
        require("luasnip.loaders.from_vscode").lazy_load()
      end,
    },
    opts = {
      history = true,
      delete_check_events = "TextChanged",
    },
    -- stylua: ignore
    keys = {
      {
        "<tab>",
        function()
          return require("luasnip").jumpable(1) and "<Plug>luasnip-jump-next" or "<tab>"
        end,
        expr = true,
        silent = true,
        mode = "i",
      },
      { "<tab>",   function() require("luasnip").jump(1) end,  mode = "s" },
      { "<s-tab>", function() require("luasnip").jump(-1) end, mode = { "i", "s" } },
    },
  },

  -- auto completion
  {
    "hrsh7th/nvim-cmp",
    version = false, -- last release is way too old
    event = "InsertEnter",
    dependencies = {
      "hrsh7th/cmp-nvim-lsp",
      "hrsh7th/cmp-buffer",
      "hrsh7th/cmp-path",
      "saadparwaiz1/cmp_luasnip",
    },
    opts = function()
      local cmp = require("cmp")
      return {
        completion = {
          completeopt = "menu,menuone,noinsert",
        },
        snippet = {
          expand = function(args)
            require("luasnip").lsp_expand(args.body)
          end,
        },
        mapping = cmp.mapping.preset.insert({
          ["<C-n>"] = cmp.mapping.select_next_item({ behavior = cmp.SelectBehavior.Insert }),
          ["<C-p>"] = cmp.mapping.select_prev_item({ behavior = cmp.SelectBehavior.Insert }),
          ["<C-b>"] = cmp.mapping.scroll_docs(-4),
          ["<C-f>"] = cmp.mapping.scroll_docs(4),
          ["<C-Space>"] = cmp.mapping.complete(),
          ["<C-e>"] = cmp.mapping.abort(),
          ["<CR>"] = cmp.mapping.confirm({ select = true }), -- Accept currently selected item. Set `select` to `false` to only confirm explicitly selected items.
          ["<S-CR>"] = cmp.mapping.confirm({
            behavior = cmp.ConfirmBehavior.Replace,
            select = true,
          }), -- Accept currently selected item. Set `select` to `false` to only confirm explicitly selected items.
        }),
        sources = cmp.config.sources({
          { name = "nvim_lsp" },
          { name = "luasnip" },
          { name = "buffer" },
          { name = "path" },
        }),
        formatting = {
          format = function(_, item)
            local icons = require("util.icons").kinds
            if icons[item.kind] then
              item.kind = icons[item.kind] .. item.kind
            end
            return item
          end,
        },
        experimental = {
          ghost_text = {
            hl_group = "LspCodeLens",
          },
        },
      }
    end,
  },

  {
    "glepnir/lspsaga.nvim",
    event = "BufRead",
    config = function()
      require("lspsaga").setup({})
    end,
    keys = {
      { "<leader>gd", "<cmd>Lspsaga peek_definition<CR>", desc = "Lspsaga Preview definition" },
      { "<leader>gD", "<cmd>Lspsaga goto_definition<CR>", desc = "Lspsaga Goto definition" },
      { "<leader>ga", "<cmd>Lspsaga code_action<CR>",  mode = { "n", "v" },   desc = "Lspsaga Code action" },
      { "<leader>gf", "<cmd>Lspsaga lsp_finder<CR>", desc = "Lspsaga Show reference"},
      { "<leader>K", "<cmd>Lspsaga hover_doc<CR>", desc = "Lspsaga Show doc"},
      { "<leader>gr", "<cmd>Lspsaga rename<CR>", desc = "Lspsaga Rename in file range"},
      { "<leader>gR", "<cmd>Lspsaga rename ++project<CR>", desc = "Lspsaga Rename in project range"},
      { "<leader>g[", "<cmd>Lspsaga diagnostic_jump_prev", desc = "Lspsaga Prev diagnostic"},
      { "<leader>g]", "<cmd>Lspsaga diagnostic_jump_next", desc = "Lspsaga Next diagnostic"},
      { "<leader>go", "<cmd>Lspsaga outline<CR>", desc = "Lspsaga Toggle outline"},
      { "<leader>ci", "<cmd>Lspsaga incoming_calls<CR>", desc = "Lspsaga Show incoming calls"},
      { "<leader>co", "<cmd>Lspsaga outgoing_calls<CR>", desc = "Lspsaga Show outgoing calls"},

    },
    dependencies = {
      { "nvim-tree/nvim-web-devicons" },
      --Please make sure you install markdown and markdown_inline parser
      { "nvim-treesitter/nvim-treesitter" }
    }
  }
}
